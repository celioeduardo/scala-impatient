 * Existem alguns detalhes técnicos.
 *   1) <=, >=, e != não são operadores de atribuição
 *   2) Um operação começando com um = nunca é um operador de atribuição
 *   	(==, ===, =/=, etc...)
 *   3) Se a tem um método chamado operador=, então esse método é chamado
